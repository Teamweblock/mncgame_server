// models/ConnectMessage.js
const mongoose = require("mongoose");

const connectMessageSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    trim: true,
    lowercase: true,
  },
  subject: {
    type: String,
    required: true,
    trim: true,
  },
  message: {
    type: String,
    required: true,
    trim: true,
  }
},
  { timestamps: true }
);

module.exports = mongoose.model("ConnectMessage", connectMessageSchema);
